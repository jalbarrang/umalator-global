/** Highest badge label whose threshold is <= value (clamps below 0 to lowest). */
export function rankLabel(value: number): string {
  let label = RANK_BADGES[0][1];
  for (const [min, name] of RANK_BADGES) {
    if (value >= min) {
      label = name;
    } else {
      break;
    }
  }
  return label;
}
